package cz.zcu.kiv.fjp.core.entities;

public class EdgeAttribute {

    String key;
    String value;

    public EdgeAttribute(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
